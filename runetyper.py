class KeyLogger():
    def __init__(self, filename: str = "keylogs.txt") -> None:
        self.filename = filename

    def get_char(key):
        try:
            return key.char
        except AttributeError:
            return str(key)

    def on_press(self, key):
        
        key = str(key)
        key = key.strip("'").lower()
        try:
            print('\r'+f'{Fore.WHITE}[ {Fore.CYAN}\u00A7 {Fore.WHITE}] {Fore.LIGHTBLACK_EX}{key} TO {map[key]} |                     ', end='')
            pyautogui.press('backspace')
            keyboards.type(map[key])
        except:
            pass

    def main(self):
        listener = keyboard.Listener(
            on_press=self.on_press,
            on_release=time.sleep(1)
        )
        listener.start()


if __name__ == '__main__':
    keyboards = keyboard.Controller()
    logger = KeyLogger()
    logger.main()
    input()
